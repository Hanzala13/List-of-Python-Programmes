import numpy as np

# Example dataset
data = [12, 15, 20, 22, 24, 30, 32, 35]

# Calculate mean
mean = np.mean(data)

# Calculate median
median = np.median(data)

# Calculate standard deviation
std_dev = np.std(data)

# Print results
print("Mean:", mean)
print("Median:", median)
print("Standard Deviation:", std_dev)
